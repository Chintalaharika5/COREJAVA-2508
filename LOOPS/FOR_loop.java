package LOOPS;

public class FOR_loop {
    public static void main(String[] args) {
        for (int i=0;i<=10;i++) {
            System.out.println("Hi");
        }

        // fill 5 water bottles
        for(int i=1;i<=5;i++) {
            System.out.println("Filling Water Bottle: "+i);

            if(i==2){
                break;
            
            }
        }
    }
}
    

